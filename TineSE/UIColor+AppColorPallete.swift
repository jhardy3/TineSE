import Foundation
import UIKit

extension UIColor {
    
    static func rustyTruckGreen() -> UIColor {
        return UIColor(red: 0.741, green: 0.784, blue: 0.518, alpha: 1.00)
    }
    
    static func desertFloorTan() -> UIColor {
        return UIColor(red: 0.953, green: 0.886, blue: 0.784, alpha: 1.00)
    }
    
    static func desertSkyBlue() -> UIColor {
        return UIColor(red: 0.478, green: 0.761, blue: 0.831, alpha: 1.00)
    }
    
    static func hunterOrange() -> UIColor {
        return UIColor(red: 1.000, green: 0.400, blue: 0.000, alpha: 1.00)
    }

    
    
}